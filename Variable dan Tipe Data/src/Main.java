public class Main {
    public static void main (String[] args) {
        System.out.println("Hello, World!");

        //String = Tipe data string adalah kumpulan karakter (huruf, angka, simbol, spasi)
        //yang diapit oleh tanda kutip (biasanya petik dua "), digunakan untuk menyimpan teks seperti nama, alamat, atau kalimat.

        String iniString;
        iniString = "iniString";
        String namaMaster = "Hafid Fathurrohman";

        System.out.println("Master dari project ini adalah " + namaMaster);

        //int =  jenis data dalam pemrograman yang digunakan untuk menyimpan nilai
        // bilangan bulat, baik positif maupun negatif, tanpa komponen pecahan atau desimal

       int x = 9;
       int y = 10;
       System.out.println("Master punya mobil sebanyak : ");

        //double = Tipe data double (presisi ganda) adalah jenis data numerik yang digunakan
        // untuk menyimpan angka pecahan atau desimal dengan ketelitian tinggi, berukuran 64-bit.

        double phi;
        phi = 3.14;

        //float = Tipe data float (floating-point) adalah
        // jenis tipe data numerik dalam pemrograman yang digunakan untuk merepresentasikan angka desimal atau pecahan.
        // Tipe ini umum digunakan untuk perhitungan matematika
        // yang membutuhkan presisi tinggi, seperti pada perhitungan pecahan, sains, atau keuangan.

        //int z = 5.7; --> ini itu tidak bisa
        float z = 5.711f;

        // char = Tipe data untuk menyimpan 1 karakter
        char inisialCrush = 'Z';

        // boolean = Tipe data yang digunakan untuk benar dan salah
        boolean apakahAkuKweren = true;

        // long = Tipe data yang digunakan untuk bilangan bulat besar
        long wargaIndonesia = 340000000L;

        // short = Tipe data untuk bilangan bulat kecil kayak 1000
        short car = 4;

        // byte = Tipe data yang digunakan untuk bilangan bulat yang amat kecil kayak 5
        byte mobil = 4;

        System.out.println(mobil + " Mobil");

    }
}